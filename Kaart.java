// Kaart.java
public class Kaart {
    private String kusimus;
    private String vastus;

    public Kaart(String kusimus, String vastus) {
        this.kusimus = kusimus;
        this.vastus = vastus;
    }

    public String getKusimus() {
        return kusimus;
    }

    public String getVastus() {
        return vastus;
    }
}
